package build

import (
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"strings"
	"testing"
)

// TestEmitGoStampsAndParses writes a small TypeScript entry, emits its Go, and
// checks the two things a golden depends on: the header names the bento build
// that produced the source, and the source below the header is a Go program the
// parser accepts. If either breaks, a checked-in golden would either lose its
// provenance or stop being valid Go, so both are pinned here.
func TestEmitGoStampsAndParses(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "hello.ts")
	if err := os.WriteFile(entry, []byte("console.log(1 + 2)\n"), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	source, err := EmitGo(entry, "v9.9.9 (commit deadbee)")
	if err != nil {
		t.Fatalf("EmitGo: %v", err)
	}

	firstLine := source[:strings.IndexByte(source, '\n')]
	if !strings.HasPrefix(firstLine, "// Code generated by bento v9.9.9 (commit deadbee) from hello.ts.") {
		t.Fatalf("header does not stamp the build and entry: %q", firstLine)
	}
	if !strings.HasSuffix(firstLine, "DO NOT EDIT.") {
		t.Fatalf("header is not the generated-code marker: %q", firstLine)
	}

	if _, err := parser.ParseFile(token.NewFileSet(), "golden.go", source, parser.AllErrors); err != nil {
		t.Fatalf("emitted source does not parse as Go: %v", err)
	}
}
