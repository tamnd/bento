// TestEmitGoStampsAndParses writes a small TypeScript entry, emits its Go, and
// checks the two things a golden depends on: the header names the bento build
// that produced the source, and the source below the header is a Go program the
// parser accepts. If either breaks, a checked-in golden would either lose its
// provenance or stop being valid Go, so both are pinned here.
func TestEmitGoStampsAndParses(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "hello.ts")
	if err := os.WriteFile(entry, []byte("console.log(1 + 2)\n"), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	source, err := EmitGo(entry, "v9.9.9 (commit deadbee)")
	if err != nil {
		t.Fatalf("EmitGo: %v", err)
	}

	firstLine := source[:strings.IndexByte(source, '\n')]
	if !strings.HasPrefix(firstLine, "// Code generated by bento v9.9.9 (commit deadbee) from hello.ts.") {
		t.Fatalf("header does not stamp the build and entry: %q", firstLine)
	}
	if !strings.HasSuffix(firstLine, "DO NOT EDIT.") {
		t.Fatalf("header is not the generated-code marker: %q", firstLine)
	}

	if _, err := parser.ParseFile(token.NewFileSet(), "golden.go", source, parser.AllErrors); err != nil {
		t.Fatalf("emitted source does not parse as Go: %v", err)
	}
}

// TestNoImplicitAnyGatesUnderProjectSetting proves the implicit-any toleration is
// scoped to the default project: an untyped parameter lowers under bento's
// default, where noImplicitAny is off and the checker's report is bento's own
// added strictness, but hands back once the project asked for noImplicitAny,
// where the same report is a rejection TypeScript stands behind. Emitting running
// Go for a program noImplicitAny refused would be unsound, so honoring the
// project setting is what keeps the AOT gate matched to the checker.
func TestNoImplicitAnyGatesUnderProjectSetting(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "untyped.ts")
	// A parameter with no annotation is the untyped form the checker reports as
	// 7006 under noImplicitAny; it resolves to `any` either way.
	if err := os.WriteFile(entry, []byte("function id(x) { return x; }\nconsole.log(id(1));\n"), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	if _, err := EmitGo(entry, "test"); err != nil {
		t.Fatalf("untyped parameter handed back under the default project, want a lowering: %v", err)
	}

	_, err := EmitGoWithOptions(entry, "test", EmitOptions{NoImplicitAny: true})
	if err == nil {
		t.Fatal("untyped parameter lowered under noImplicitAny, want a hand-back")
	}
	if !strings.Contains(err.Error(), "implicitly has an 'any' type") {
		t.Fatalf("hand-back reason = %q, want the checker's implicit-any message", err.Error())
	}
}

// TestAllowUnreachableCodeGates proves the project's allowUnreachableCode setting
// reaches the checker: an exhaustive switch that leaves a trailing statement
// unreachable lowers under bento's default, where reachability is not a build
// error, but hands back once the project set allowUnreachableCode false, where
// the checker reports the dead statement. Emitting Go for a program the project
// rejects would be unsound, so threading the option is what matches the AOT gate
// to the checker.
func TestAllowUnreachableCodeGates(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "unreachable.ts")
	src := "const f = (x: any): number => {\n" +
		"  switch (typeof x) {\n" +
		"    case 'string': return 0\n" +
		"    case 'number': return 0\n" +
		"    case 'bigint': return 0\n" +
		"    case 'boolean': return 0\n" +
		"    case 'symbol': return 0\n" +
		"    case 'undefined': return 0\n" +
		"    case 'object': return 0\n" +
		"    case 'function': return 0\n" +
		"  }\n" +
		"  x;\n" +
		"}\n"
	if err := os.WriteFile(entry, []byte(src), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	if _, err := EmitGo(entry, "test"); err != nil {
		t.Fatalf("unreachable statement handed back under the default project, want a lowering: %v", err)
	}

	deny := false
	_, err := EmitGoWithOptions(entry, "test", EmitOptions{AllowUnreachableCode: &deny})
	if err == nil {
		t.Fatal("unreachable statement lowered under allowUnreachableCode:false, want a hand-back")
	}
	if !strings.Contains(err.Error(), "Unreachable code") {
		t.Fatalf("hand-back reason = %q, want the checker's unreachable-code message", err.Error())
	}
}

// TestConstructSignatureArityGates proves an assignability error whose
// elaboration is a signature arity mismatch hands back rather than lowering under
// the assignability toleration. Assigning a class to a construct-signature type
// that supplies fewer constructor arguments than the class requires is a TS2322
// the checker elaborates with TS2849 ("Target signature provides too few
// arguments"); unlike the widened-literal and cross-primitive coercions the
// toleration admits, a signature-arity mismatch has no value to land, so emitting
// Go for it would ship a program TypeScript rejects with no run-time coercion to
// stand behind. Honoring the arity report is what keeps the toleration matched to
// real value coercions.
func TestConstructSignatureArityGates(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "arity.ts")
	// Foo's constructor needs an argument the target construct signature never
	// passes, the TS2322 + TS2849 shape.
	src := "class Foo {\n  constructor(x: number) {}\n}\nconst foo: { new(): Foo } = Foo;\n"
	if err := os.WriteFile(entry, []byte(src), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	_, err := EmitGo(entry, "test")
	if err == nil {
		t.Fatal("construct-signature arity mismatch lowered, want a hand-back")
	}
	if !strings.Contains(err.Error(), "not assignable") {
		t.Fatalf("hand-back reason = %q, want the checker's not-assignable message", err.Error())
	}
}

// TestObjectTypeWithSymbolMemberDoesNotPanic proves an object literal checked
// against the global Object type under the es2015 lib lowers without panicking.
// That Object type carries symbol-keyed members ([Symbol.hasInstance]), whose
// checker-internal names are not valid UTF-8. Before the fix the symbol member
// mangled into a struct field whose json tag held the raw non-UTF-8 key, and the
// nested intern that failed later rolled its reservation back by popping the
// emission order's tail, which dropped a sibling struct and stranded a name with
// a nil declaration node, a nil that panicked go/ast.Walk. Interning now declines
// the symbol-keyed shape by name, so the program lowers cleanly rather than
// emitting a struct that does not print or crashing the walk.
func TestObjectTypeWithSymbolMemberDoesNotPanic(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "objsym.ts")
	src := "var a = { toString: 5 };\nvar c: Object = a;\n"
	if err := os.WriteFile(entry, []byte(src), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	defer func() {
		if p := recover(); p != nil {
			t.Fatalf("lowering panicked on an Object-typed binding under es2015: %v", p)
		}
	}()
	if _, err := EmitGoWithOptions(entry, "test", EmitOptions{Target: "es2015"}); err != nil {
		t.Fatalf("Object-typed binding handed back under es2015, want a lowering: %v", err)
	}
}

// TestStrictOffNoImplicitAnyWidensToAny proves bento reproduces a project that
// turned strict off while keeping noImplicitAny on. A destructuring binding from
// an all-undefined-and-null initializer, var [a, b] = [undefined, null], is legal
// under bento's forced-strict default: strictNullChecks keeps undefined and null
// their own types, so the binding elements are not implicitly any and the case
// lowers. Under the project's real options the two widen to any and the binding
// elements are implicitly any, the TS7031 the checker masks when strict null
// checking is on. Setting Strict false with NoImplicitAny keeps the checker
// matched to the project so the masked report surfaces and the unit hands back
// rather than running a program TypeScript rejects.
func TestStrictOffNoImplicitAnyWidensToAny(t *testing.T) {
	dir := t.TempDir()
	entry := filepath.Join(dir, "widen.ts")
	if err := os.WriteFile(entry, []byte("var [a, b] = [undefined, null];\n"), 0o644); err != nil {
		t.Fatalf("write entry: %v", err)
	}

	if _, err := EmitGo(entry, "test"); err != nil {
		t.Fatalf("widened binding handed back under the strict default, want a lowering: %v", err)
	}

	off := false
	_, err := EmitGoWithOptions(entry, "test", EmitOptions{Strict: &off, NoImplicitAny: true})
	if err == nil {
		t.Fatal("widened binding lowered under strict-off noImplicitAny, want a hand-back")
	}
	if !strings.Contains(err.Error(), "implicitly has an 'any' type") {
		t.Fatalf("hand-back reason = %q, want the checker's implicit-any message", err.Error())
	}
}
